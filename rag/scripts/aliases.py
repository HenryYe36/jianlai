"""角色别名表——索引时和检索时共用的单一事实源.

ROLES: 主角色 → 索引时使用的 needles (任一命中即视为该角色出场).
CO_PRESENT: 配角 → 别名列表 (检测某个配角是否在场时, 任一别名命中即算到场).

设计原则:
- needles 要"独占性高 + 召回足够"; 太通用的称谓 (如 "先生") 不放进去, 避免污染.
- 同一灵魂的分身计入同一索引 (崔瀺 = 崔诚 = 绣虎 = 大骊国师);
  独立人格的分身 (崔东山) 单独成角色, 不并入.
- 早期/晚期改名的同一角色, 都列出 (裴钱 = 不要钱; 顾粲 = 顾璨).
"""
