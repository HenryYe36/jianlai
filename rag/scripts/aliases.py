def needles_for(role: str) -> list[str]:
    """返回主角色的索引 needles."""
    return ROLES.get(role, [role])


def is_present(name: str, text: str) -> bool:
    """根据别名表判定 name 是否在 text 中出现."""
    aliases = CO_PRESENT.get(name, [name])
    return any(a in text for a in aliases)


def co_present_pool() -> list[str]:
    """规范名列表 (用于扫描时遍历检测)."""
    return list(CO_PRESENT.keys())
